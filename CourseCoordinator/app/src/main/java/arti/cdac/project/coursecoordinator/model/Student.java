package arti.cdac.project.coursecoordinator.model;

public class Student {
    String stud_name;
    String stud_email;
    String stud_propic;

    public Student() {
    }

    public String getStud_name() {
        return stud_name;
    }

    public void setStud_name(String stud_name) {
        this.stud_name = stud_name;
    }

    public String getStud_email() {
        return stud_email;
    }

    public void setStud_email(String stud_email) {
        this.stud_email = stud_email;
    }

    public String getStud_propic() {
        return stud_propic;
    }

    public void setStud_propic(String stud_propic) {
        this.stud_propic = stud_propic;
    }
}
